char	*ft_strcapitalize(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 'A' && str[i] <= 'Z')
			str[i] += 32;
			i++;

			(str[0] >= 'a' && str[0] <= 'z');
			str[0] -= 32;
	}
	i = 0;
	while (str[i])
	{
		if ((str[i] >= 32 && str[i] <= 64)
				&& !(str[i] >= '0' && str[i] <= '9')
				&& (str[i + 1] >= 'a' && str[i + 1] <= 'z'))
		{
			str[i + 1] = str[i + 1] - 32;
			i++;
		}
		else
		i++;
	}
	return (str);
}
